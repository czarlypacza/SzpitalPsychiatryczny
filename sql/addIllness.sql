CREATE OR ALTER PROCEDURE ADDILLNESS @NAME VARCHAR(
    60
),
@DESCRIPTION VARCHAR(
    150
) AS DECLARE @ILOSC INT;

SET @ILOSC = (
    SELECT
        COUNT(id)
    FROM
        illnesses
	WHERE
		name = @name
)

IF (@ILOSC=0)

BEGIN
    INSERT INTO ILLNESSES (
        NAME,
        DESCRIPTION
    ) VALUES (
        @NAME,
        @DESCRIPTION
    );
END